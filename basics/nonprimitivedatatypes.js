// Non primitive datatypes
// Array
const cars = ["Saab", "Volvo", "BMW"];
console.log(cars);
console.log(typeof(cars));
// object
let myObject = {
    Name :  "Shyam",
    age : 22,
};
console.log(myObject);
console.log(typeof(myObject));
// function
let myFunction = {
    a : 33
}
console.log(myFunction);
console.log(typeof(myFunction));