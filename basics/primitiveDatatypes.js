// datatyeps can be primitive and non primitive based on the memory arrangement
// primitive datatypes
// number
let number = 123
console.log(number);
console.log(typeof(number));
// string
 let string = "Shyam Verma "
 console.log(string);
console.log(typeof(string));
// boolean
let bool = false
console.log(bool);
console.log(typeof(bool));
// null
let empty = null
console.log(empty);
console.log(typeof(empty));
// undefined
let notDefined = undefined
console.log(notDefined);
console.log(typeof(notDefined)); 
// symbol
let id  = Symbol('13') 
console.log(id);
console.log(typeof(id));
// BigInt
let bigInt = 22222222433435n
console.log(bigInt);
console.log(typeof(bigInt));
