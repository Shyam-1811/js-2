// Switch case statements
const day = 3;
switch (day) {
    case 1:
        console.log("Monday")
        break;
        case 2:
            console.log("tuesday")
            break;
            case 3:
                console.log("Wednesday")
                break;
    default:
        console.log("not a day ")
        break;
}

